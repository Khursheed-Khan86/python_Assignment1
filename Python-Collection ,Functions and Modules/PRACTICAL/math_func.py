 #Write a Python program to demonstrate the use of functions from the math module. 
import math
print(f"Square root of 4 is {math.sqrt(4)}")
print(f"Factorial of 5 is {math.factorial(5)}")
print(f"3 raised to the power 4 is {math.pow(3,4)}")
print(f"Roundoff downward value of 46.45 is {math.floor(46.45)}")
print(f"Roundoff upward value of 46.44 is {math.ceil(46.44)}")