#Write a Python program to remove elements from a list using pop() and remove().
l1=[1,2,3,4,5,6,7,7,8]
print(l1)
l1.pop()#remove last element from the list
print(l1)
l1.remove(7)#remove 7 from the list
print(l1)
l1.remove(2)#remove 2 from the list
print(l1)
l1.pop(0)#remove element at 0 position
print(l1)
